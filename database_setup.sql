CREATE TABLE USERS (
    USER_ID INT IDENTITY(1,1) PRIMARY KEY,
    FULL_NAME NVARCHAR(150) NOT NULL,
    PHONE NVARCHAR(20) NOT NULL,
    EMAIL NVARCHAR(100) NOT NULL UNIQUE,
    LOGIN NVARCHAR(50) NOT NULL UNIQUE,
    PASSWORD CHAR(40) NOT NULL
);
GO

CREATE TABLE POSITIONS (
    POSITION_ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(50) NOT NULL UNIQUE
);
GO

CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID INT PRIMARY KEY,
    POSITION_ID INT NOT NULL,
    CONSTRAINT FK_EMPLOYEES_USERS FOREIGN KEY (EMPLOYEE_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT FK_EMPLOYEES_POSITIONS FOREIGN KEY (POSITION_ID) REFERENCES POSITIONS(POSITION_ID)
);
GO

CREATE TABLE CUSTOMERS (
    CUSTOMER_ID INT IDENTITY(1,1) PRIMARY KEY,
    USER_ID INT NOT NULL UNIQUE,
    DELIVERY_ADDRESS NVARCHAR(250) NOT NULL,
    CONSTRAINT FK_CUSTOMERS_USERS FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);
GO

CREATE TABLE ORDER_STATUSES (
    ORDER_STATUS_ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(50) NOT NULL UNIQUE
);
GO

CREATE TABLE PRODUCT_CATEGORIES (
    CATEGORY_ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(100) NOT NULL UNIQUE
);
GO

CREATE TABLE PRODUCTS (
    PRODUCT_ID INT IDENTITY(1,1) PRIMARY KEY,
    CATEGORY_ID INT NOT NULL,
    NAME NVARCHAR(150) NOT NULL,
    CURRENT_SALE_PRICE DECIMAL(10,2) NOT NULL,
    CURRENT_PURCHASE_PRICE DECIMAL(10,2) NOT NULL,
    DESCRIPTION NVARCHAR(MAX),
    IMAGE NVARCHAR(255),
    MANUFACTURER NVARCHAR(150),
    EXECUTION_TYPE NVARCHAR(100),
    SYSTEM_CLASS NVARCHAR(100),
    MONITOR_SIZE NVARCHAR(100),
    ACTIVE_PORTS NVARCHAR(200),
    WEIGHT NVARCHAR(50),
    CONSTRAINT FK_PRODUCTS_CATEGORIES FOREIGN KEY (CATEGORY_ID) REFERENCES PRODUCT_CATEGORIES(CATEGORY_ID)
);
GO

CREATE TABLE ORDERS (
    ORDER_ID INT IDENTITY(1,1) PRIMARY KEY,
    CUSTOMER_ID INT NOT NULL,
    ORDER_DATE DATETIME NOT NULL DEFAULT GETDATE(),
    ORDER_STATUS_ID INT NOT NULL,
    CONSTRAINT FK_ORDERS_CUSTOMERS FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
    CONSTRAINT FK_ORDERS_STATUSES FOREIGN KEY (ORDER_STATUS_ID) REFERENCES ORDER_STATUSES(ORDER_STATUS_ID)
);
GO

CREATE TABLE ORDER_ITEMS (
    ORDER_ID INT NOT NULL,
    PRODUCT_ID INT NOT NULL,
    SALE_PRICE_AT_TIME DECIMAL(10,2) NOT NULL,
    QUANTITY INT NOT NULL CHECK (QUANTITY > 0),
    PRIMARY KEY (ORDER_ID, PRODUCT_ID),
    CONSTRAINT FK_ORDER_ITEMS_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID),
    CONSTRAINT FK_ORDER_ITEMS_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(PRODUCT_ID)
);
GO

CREATE TABLE PAYMENT_STATUSES (
    PAYMENT_STATUS_ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(50) NOT NULL UNIQUE
);
GO

CREATE TABLE PAYMENTS (
    PAYMENT_ID INT IDENTITY(1,1) PRIMARY KEY,
    ORDER_ID INT NOT NULL,
    PAYMENT_METHOD NVARCHAR(50) NOT NULL,
    PAYMENT_STATUS_ID INT NOT NULL,
    CONSTRAINT FK_PAYMENTS_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID),
    CONSTRAINT FK_PAYMENTS_STATUS FOREIGN KEY (PAYMENT_STATUS_ID) REFERENCES PAYMENT_STATUSES(PAYMENT_STATUS_ID)
);
GO

CREATE TABLE SUPPLIERS (
    SUPPLIER_ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(150) NOT NULL UNIQUE,
    LEGAL_ADDRESS NVARCHAR(250) NOT NULL
);
GO

CREATE TABLE SUPPLIES (
    SUPPLY_ID INT IDENTITY(1,1) PRIMARY KEY,
    SUPPLIER_ID INT NOT NULL,
    SUPPLY_DATE DATETIME NOT NULL DEFAULT GETDATE(),
    CONSTRAINT FK_SUPPLIES_SUPPLIER FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIERS(SUPPLIER_ID)
);
GO

CREATE TABLE SUPPLY_ITEMS (
    SUPPLY_ID INT NOT NULL,
    PRODUCT_ID INT NOT NULL,
    PURCHASE_PRICE_AT_TIME DECIMAL(10,2) NOT NULL,
    QUANTITY INT NOT NULL CHECK (QUANTITY > 0),
    PRIMARY KEY (SUPPLY_ID, PRODUCT_ID),
    CONSTRAINT FK_SUPPLY_ITEMS_SUPPLY FOREIGN KEY (SUPPLY_ID) REFERENCES SUPPLIES(SUPPLY_ID),
    CONSTRAINT FK_SUPPLY_ITEMS_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(PRODUCT_ID)
);
GO

